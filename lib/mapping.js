// 「(ひろ)」接頭辞を無視するためのヘルパー。
export function stripHiro(s) {
  return (s || '').replace(/^\(ひろ\)/, '').trim();
}

// ----------------------------------------------------------------------------
// Zaim の1行を分類する。
//   引数 row: { type, category, subCategory }  ※type は 'income' | 'expense' | 'investment'
//   返り値: { type, major, minor, matched }
// ----------------------------------------------------------------------------
export function classifyZaim(type, category, subCategory) {
  const cat = stripHiro(category);
  const sub = stripHiro(subCategory);

  // 投資（積立）は支出に含めず別集計。
  if (type === 'investment' || cat === '投資') {
    return { type: 'investment', major: '投資', minor: sub || '積立', matched: true };
  }
  // 収入は大分類にマッピングせず「収入」として別集計。
  if (type === 'income') {
    return { type: 'income', major: '収入', minor: sub || cat || '各種収入', matched: true };
  }
  // 支出
  const hit = zaimMap[`${cat}|${sub}`];
  if (hit) return { type: 'expense', major: hit[0], minor: hit[1], matched: true };
  return { type: 'expense', major: UNMAPPED, minor: sub || cat || '(空白)', matched: false };
}

// ----------------------------------------------------------------------------
// MoneyForward の1行を分類する。
//   引数: type ('income' | 'expense'), major(大項目), minor(中項目)
// ----------------------------------------------------------------------------
export function classifyMf(type, major, minor) {
  const cat = (major || '').trim();
  const sub = (minor || '').trim();

  if (type === 'income') {
    return { type: 'income', major: '収入', minor: sub || cat || '各種収入', matched: true };
  }
  const hit = mfMap[`${cat}|${sub}`];
  if (hit) return { type: 'expense', major: hit[0], minor: hit[1], matched: true };
  return { type: 'expense', major: UNMAPPED, minor: sub || cat || '(空白)', matched: false };
}
